ll Solve(ll n,vector < ll> v1){
    if (v1.size() ==1 || ( v1.size() ==2 )) return -1;
    sort(all(v1));
    ll mid=v1[n/2];
    ll sum=0;
    for (auto &x : v1){
        sum+=x;
    }
    ll x=((n*2)* (mid));
    return ( 0 > x-sum+1? 0  : x-sum+1);
}

