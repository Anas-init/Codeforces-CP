int Solve(vector<int> temp,int n,int k){
    int ans=0;
    int cnt=0;
    for ( int i=0;i<temp.size();i++ ){
       if(temp[i]>=k){
        ans+=temp[i];
       }
       if(temp[i]==0 && ans>0){
        ans--;
        cnt++;
       }
    }
    return cnt;
    
}

